/* ● La Herencia nos permite crear una subclase o clase hija que posea automáticamente las 
propiedades y métodos de la superclase o clase padre.
● Un constructor o un método para que puedan llamar a miembros de la clase padre debe 
utilizar la palabra reservada “super”.
● En el caso que la clase hija no disponga de un constructor, siempre se incluye un constructor 
por defecto, que llama al constructor de la clase padre con los parámetros que se hayan 
pasado al realizar la instancia con new. 
● Existe la posibilidad de hacer herencia multinivel, es decir, tener clases padres, hijos, nietos y 
así sucesivamente.
● Recordar que los objetos van dejando disponibles sus prototipos a medida que le vamos 
indicando a cada objeto que tendrá acceso a otro. */